// Send printf to uart1
int _write(int fd, char* ptr, int len) {
  HAL_StatusTypeDef hstatus;

  if (fd == 1 || fd == 2) {
    hstatus = HAL_UART_Transmit(&huart2, (uint8_t *) ptr, len, HAL_MAX_DELAY);
    if (hstatus == HAL_OK)
      return len;
    else
      return -1;
  }
  return -1;
}


// Function HAL_TIM_PeriodElapsedCallback is called when TimerX reaches the end of its period
inline void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	// Checking if the callback was invoked by timer 6 (i.e. htim points to timer 6)
	if (htim->Instance == htim6.Instance)
	{
		++cb_cnt; // Update callback counter. Timer6 freq is set on 10000 Hz, so cb_cnt
		// should be incremented 10000 times per second.
	}
}

void fill_dac_buffer()
{
	for (int i = 0; i < DMA_BUFFER_SIZE; i++)
	{
		// Sawtooth graph:
		//dma_buffer[i] = i * 10; // Fill the buffer which is later passed to the DAC

		// Sinus graph:
		dma_buffer[i] = ((sin(i * 2 * M_PI / DMA_BUFFER_SIZE) + 1)) * 4096 / 4;
	}
}

// The function is called when the DAC conversion on channel 1 is completed
inline void HAL_DAC_ConvCpltCallbackCh1(DAC_HandleTypeDef *hdac)
{
	++cb_full; // Increment pointer which tells that buffer is full
}


// The function is called when half of the DAC conversion on channel 1 is completed
inline void HAL_DAC_ConvHalfCpltCallbackCh1(DAC_HandleTypeDef *hdac)
{
	++cb_half;
}

