// Send printf to uart1
int _write(int fd, char* ptr, int len)
{
	if (fd == 1)
	{
		HAL_StatusTypeDef hstatus;
		hstatus = HAL_UART_Transmit(&huart2, (uint8_t *) ptr, len, HAL_MAX_DELAY);
		if (hstatus == HAL_OK)
			return len;
		else
			return -1;
	} else
		return -1;

	return -1;
}

